number_1 = float(input("Enter a Number: "))
number_2 = float(input("Enter a Second Number: "))
Calculator = input("Add, Subtract, Multiply or Divide:")

if Calculator == "Add":
    print(number_1 + number_2)
elif Calculator == "Subtract":
    print(number_1 - number_2)
elif Calculator == "Multiply":
    print(number_1 * number_2)
else:
    print(number_1 / number_2)


